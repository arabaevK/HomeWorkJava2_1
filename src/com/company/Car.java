package com.company;

public class Car {
    private String motor;
    private String kkp;

    public Car(String motor, String kkp) {
        this.motor = motor;
        this.kkp = kkp;
    }

    public String getMotor() {
        return motor;
    }

    public String getKkp() {
        return kkp;
    }

}
